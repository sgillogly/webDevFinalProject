        <h2>What are dreams?</h2>
        <p>&#160;Dreams are events that happen within the mind while someone is sleeping. They usually occur during the REM stage of sleep which involves memory consolidation, learning, emotional processing, and emotional regulation (Spielman et al. 2020). There are many theories as to what dreams actually are and what they represent to us. Sigmund Freud believed that dreams had manifest content (the storyline/plot of a dream) and latent content (the hidden meaning of a dream) which helped with studying what dreams could be (Spielman et al. 2020). Another theorist, Rosalind Cartwright, “believes that dreams simply reflect life events that are important to the dreamer” (Spielman et al. 2020). This interpretation is important to understanding what the latent content of dreams may be as many events that occur in dreams cannot and could not happen in real life. This theory is also important to the memory consolidation side of REM sleep as when our dreams occur, we may be manifesting the events of the dream from life events that are important to us.</p>
