        <h2>What is this website?</h2>
        <p>&#160;This website is a place for users to write down the manifest content of their dreams in order to keep track of them and possibly perceive the latent content that could present itself. For the best results, you may want to write the events of your dreams as soon as you wake up because their memory of the dream is clearest then. After being awake, the memory of the dream starts to fade or warp. Keep track of what is going on in your mind while you are dreaming and find some inspiration from the latent content that you come to interpret from what you remember experiencing.</p>
